// 名言をランダムに表示する関数
function changeQuote() {
    // 1. まず、アニメーション用のクラスを一旦削除してリセットする
    quoteDisplay.classList.remove('fade-in');

    // 2. ランダムな名言を選ぶ
    const randomIndex = Math.floor(Math.random() * quotes.length);
    const randomQuote = quotes[randomIndex];
    
    // 3. アニメーションのリセットを確実にするため、少しだけ間を置いて処理する
    setTimeout(() => {
        // 4. テキストを新しい名言に更新する
        quoteText.textContent = randomQuote.text;
        quoteAuthor.textContent = `- ${randomQuote.author}`;

        // 5. 再びクラスを追加して、アニメーションを開始させる
        quoteDisplay.classList.add('fade-in');
    }, 10); // ほんの少しの遅延
}

// ボタンがクリックされたらchangeQuote関数を実行
changeQuoteBtn.addEventListener('click', changeQuote);

// ページが最初に読み込まれたときも、ふわっと表示されるようにする
document.addEventListener('DOMContentLoaded', () => {
    quoteDisplay.classList.add('fade-in');
});